package com.example.layoutapp.bean;

public class Position {
    private float x;
    private float y;

    public Position(float x, float v) {
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
}
